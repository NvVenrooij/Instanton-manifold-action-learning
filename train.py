# Path to a dataset directory produced by data_generator.py.
DATA_DIR = Path("/your/path/here")

# Output root; runs are written to OUT_DIR/dw_cnn_N<N_tau>_<timestamp>/
OUT_DIR  = Path("results")

# --- Training ---
N_EPOCHS        = 20
BATCH_SIZE      = 64
LEARNING_RATE   = 1e-3
TRAIN_FRACTION  = 0.8
RANDOM_SEED     = 42

# --- Loss terms ---
LAMBDA_GRAD       = 10.0   # gradient matching weight        (0 = off)
USE_Z2_AUG        = False   # Z2 flip augmentation during gradient matching
LAMBDA_SADDLE     = 0.0    # saddle-point pinning weight      (0 = off)
N_SADDLE          = 5      # number of instanton profiles for saddle pinning

# --- Architecture ---
N_CHANNELS      = 32
KERNEL_SIZES    = [3, 3, 3]

# --- Checkpointing & diagnostics ---
CHECKPOINT_EVERY   = 50    # save checkpoint every N epochs (0 = off)
RUN_HESSIAN_CHECK  = True  # compute and plot Hessian spectrum at the instanton


