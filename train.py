def load_raw_dataset(data_dir: Path) -> dict:
    """
    Load configs, actions, gradients and params.json from a data directory.

    Returns a dict with keys: configs, actions, gradients, params.
    """
    if not data_dir.exists():
        raise FileNotFoundError(
            f"Dataset directory not found: {data_dir}\n"
            "Set DATA_DIR at the top of train.py and generate data with data_generator.py."
        )

    required = ["configs.npy", "actions.npy", "gradients.npy", "params.json"]
    missing  = [f for f in required if not (data_dir / f).exists()]
    if missing:
        raise FileNotFoundError(f"Missing files in {data_dir}: {missing}")

    with open(data_dir / "params.json") as f:
        params = json.load(f)

    return {
        "configs"   : np.load(data_dir / "configs.npy"),
        "actions"   : np.load(data_dir / "actions.npy"),
        "gradients" : np.load(data_dir / "gradients.npy"),
        "params"    : params,
    }


def prepare_data(raw: dict, train_frac: float, path: DoubleWellPath) -> dict:
    """
    Normalise, split, and convert to PyTorch tensors.

    Normalisation
    -------------
    Actions are shifted by S_inst (continuum) and scaled by the dataset std,
    so the network sees targets centred on the instanton action.
    Gradients are scaled by the same std (no shift).
    Configurations are left untouched (already in physical units).

    Returns a dict with keys:
        train_q, train_S, train_grad_S,
        val_q, val_S, val_grad_S,
        val_configs, val_grads_np,   <- numpy arrays kept for plotting
        S_anchor, std_S, N_tau.
    """
    configs, actions, gradients = raw["configs"], raw["actions"], raw["gradients"]
    N_total, N_tau = configs.shape

    S_anchor       = path.S_inst_continuum
    std_S          = actions.std()
    actions_norm   = (actions   - S_anchor) / std_S
    gradients_norm = gradients  / std_S

    idx     = np.random.permutation(N_total)
    n_train = int(train_frac * N_total)
    t_idx, v_idx = idx[:n_train], idx[n_train:]

    def to_q(cfg): return torch.FloatTensor(cfg).unsqueeze(1)   # (N, 1, N_tau)
    def to_s(arr): return torch.FloatTensor(arr).flatten()

    return {
        "train_q"      : to_q(configs[t_idx]),
        "train_S"      : to_s(actions_norm[t_idx]),
        "train_grad_S" : torch.FloatTensor(gradients_norm[t_idx]),
        "val_q"        : to_q(configs[v_idx]),
        "val_S"        : to_s(actions_norm[v_idx]),
        "val_grad_S"   : torch.FloatTensor(gradients_norm[v_idx]),
        "val_configs"  : configs[v_idx],           # raw numpy, for plotting
        "val_grads_np" : gradients[v_idx],          # physical units, for plotting
        "S_anchor"     : float(S_anchor),
        "std_S"        : float(std_S),
        "N_tau"        : N_tau,
    }


